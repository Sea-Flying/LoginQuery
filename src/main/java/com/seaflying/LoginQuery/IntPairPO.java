package com.seaflying.LoginQuery;

public class IntPairPO {
    private Integer A;
    private Integer B;

    public IntPairPO (int a, int b){
        A = a;
        B = b;
    }

    public Integer getA() {
        return A;
    }

    public void setA(Integer a) {
        A = a;
    }

    public Integer getB() {
        return B;
    }

    public void setB(Integer b) {
        B = b;
    }
}
